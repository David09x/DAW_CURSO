public class coche2 {

    private String modelo;
    private String marca;

    public coche2(String modelo, String marca) {
        this.modelo = modelo;
        this.marca = marca;
    }

    public coche2() {
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }


}
